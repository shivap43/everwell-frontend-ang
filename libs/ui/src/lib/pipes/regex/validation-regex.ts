/* eslint-disable no-useless-escape */

export const RegexForFieldValidation = {
    PHONE: /^\(?[2-9](?!11)([0-9]{2})\)?[-.\s]?([0-9]{3})[-.\s]?([0-9]{4})$/,
    EMAIL: /^[_A-Za-z0-9-\+]+(\.[_A-Za-z0-9-]+)*@([a-zA-Z0-9]+(-[a-zA-Z0-9]+)*)+(\.([a-zA-Z0-9]+(-[a-zA-Z0-9]+)*)+)*(\.[A-Za-z]{2,})$/,
    STATE: /^[A-Z]{2}$/,
    CITY: /^[a-zA-Z0-9-'()#&.,\s]*$/,
    NAME: /(^[a-zA-Z][a-zA-Z-'\s]*[a-zA-Z]$)|(^[a-zA-Z]$)/,
    ZIP_CODE: /^\d{4,5}([-\s]?\d{4})?$/,
    SSN: /^((?!078051120|123456789|98765432|111111111|222222222|333333333|444444444|555555555|666666666|777777777|888888888|999999999|000000000|012345678)(?!666)\d{3}\d{2}\d{4}$)|(^XXX-XX-\d{4})$|(^XXX-XX-XXXX$)|(^XXX-XX\d{1}-\d{3}$)/, // eslint-disable-line
    ADDRESS: /^[a-zA-Z0-9-'()#&.,\s]*$/,
    USERNAME: /^[a-zA-Z@].{6,100}$/,
    PASSWORD: /^((?=.*[a-z])(?=.*[A-Z])(?=.*\d)(?=.*[#^@$!%*?&])(?!.*[\s])[A-Za-z\d#^@$!%*?&]{8,20})$/,
    MIDDLENAME: /^[a-zA-Z-'\s]*$/,
    DATE: /^\d{4}-[0-1][0-9]-[0-3][0-9]$/,
    GROUP: /^[A-Za-z0-9]{5}$|^[A-Za-z0-9-]{8}$/,
};
